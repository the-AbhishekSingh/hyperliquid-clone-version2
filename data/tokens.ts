export const getTokensByCategory = (category: string) => {
  return TOKENS.filter((token) => token.category === category)
}

export const searchTokens = (query: string) => {
  const lowercaseQuery = query.toLowerCase()
  return TOKENS.filter(
    (token) =>
      token.symbol.toLowerCase().includes(lowercaseQuery) ||
      token.name.toLowerCase().includes(lowercaseQuery) ||
      token.baseAsset.toLowerCase().includes(lowercaseQuery),
  )
}

export const getTokenBySymbol = (symbol: string) => {
  return TOKENS.find((token) => token.symbol === symbol)
}
